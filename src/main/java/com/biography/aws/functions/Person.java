package com.biography.aws.functions;

public class Person {
	
	int id;
	String name;
	String address;
	int age;
	
	public Person() {
		
	}
	
	public Person(int id, String name, String address, int age) {
		super();
		this.id = id;
		this.name = name;
		this.address = address;
		this.age = age;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	@Override
	public String toString() {
		return "Product [id=" + id + ", name=" + name + ", address=" + address + ", age=" + age + "]";
	}
	

}
